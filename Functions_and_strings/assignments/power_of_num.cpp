#include "main.h"
/**
 * PowerOfNum - prints a number to the power of 2 and 3 and 4
 * @Number: a given number
 */
void PowerOfNum(float Number)
{
    cout << Number << " ^ 2 = " << pow(Number, 2) << endl;
    cout << Number << " ^ 3 = " << pow(Number, 3) << endl;
    cout << Number << " ^ 4 = " << pow(Number, 4) << endl;

}