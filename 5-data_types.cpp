#include <iostream>
using namespace std;

int main(void)
{
    cout << "The size of bool data type is: " << sizeof(bool) << " bytes\n";
    cout << "The size of char data type is: " << sizeof(char) << " bytes\n";
    cout << "The sizeof short data type is: " << sizeof(short int) << " bytes\n";

    cout << "The sizeof long int data type is: " << sizeof(long int) << " bytes\n";
    cout << "The sizeof long long data type is: " << sizeof(long long) << " bytes\n";

    cout << "The size of float data type is: " << sizeof(float) << " bytes\n";
    cout << "The size of double data type is: " << sizeof(double) << " bytes\n";


    return (0);
}